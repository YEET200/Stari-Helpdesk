/**
 *  @swagger
 *  /api/feedback_and_surveys:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Feedback_and_surveys]
 *      summary: Get all feedback_and_surveys
 *      description: Get all feedback_and_surveys
 *      responses:
 *        200:
 *          description: Feedback_and_surveys list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Feedback_and_surveys"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */

router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;

    const payload = await Feedback_and_surveysDBApi.findAll(req.query);
    if (filetype && filetype === 'csv') {
      const fields = ['id', 'title', 'content'];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

