/**
 *  @swagger
 *  /api/training_and_tutorials:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Training_and_tutorials]
 *      summary: Get all training_and_tutorials
 *      description: Get all training_and_tutorials
 *      responses:
 *        200:
 *          description: Training_and_tutorials list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Training_and_tutorials"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */

router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;

    const payload = await Training_and_tutorialsDBApi.findAll(req.query);
    if (filetype && filetype === 'csv') {
      const fields = ['id', 'title', 'description', 'start_date', 'end_date'];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

